          <h3>Description </h3>
<p>Two-year renovation and expansion (two additions) of Skillman Library, including an increase of collection capacity, additional computer and multi-media usage, upgrading the building interior and exterior and providing additional public, group and private areas, a common room, and a café.</p> 

<p>New construction of four dormitories: Fisher Hall East, Fisher Hall West, Rubin Hall and Kamine Hall; and a structured parking facility.</p>

<p>Fisher Field included a demolition of the North Stand bleachers and replacement of the South Stand bleachers, new home side seating, giant screen scoreboard, and the removal of the grass filled with a replacement of monofilament field-turf.</p>

<p>McKeen Hall consisted of a full renovation with new M/E/P/FP systems and new toilet and shower rooms for all five floors.</p>

