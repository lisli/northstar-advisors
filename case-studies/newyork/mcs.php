          <h1>Mastery Charter Schools</h1>
          <h4>Project Overview</h4>
          <p><strong>Campuses:</strong><br /> 
          Pickett, Lenfest, Shoemaker, Thomas, Smedley, Harrity, Hardy Williams, Gratz, Mann, Clymer, and Cleveland Campuses</p>
          <p><strong>Location:</strong><br /> 
          Philadelphia, PA</p>
          <p><strong>Type of Work:</strong><br /> 
          Renovation and Restoration</p>
          <p><strong>NorthStar Advisors’ Role:</strong><br /> 
          Project Manager, Owner's Representative, Budget Management, Warranty Review, and Operations, Facilities, Pre-Planning, and Cyclical Maintenance Planning</p>
          <div class="rule"></div>
          <h3>Description </h3>
<p>Renovation and restoration of Pickett and Lenfest campuses; operations, facilities, and cyclical maintenance planning of Thomas and Shoemaker campuses. Improved facilities and natural light resulted in improved test scores of students and a decrease in overall violence and poor behavior in the schools. </p>

	<p>Harrity, Hardy Williams, Gratz, Smedley, Mann, and Clymer campuses involved renovations of the walls, floors and reception areas – including everything from new paint and carpet to new construction. NorthStar managed the renovations, from painting and electrical work to the relocation and disposal of old furniture.</p>

	<p>Smedley campus includes the addition of a full height gymnasium with classrooms above and installation of air conditioning in all the existing classroom and ancillary spaces. A complete overhaul of the electrical, mechanical, plumbing and fire alarm systems was performed. NorthStar managed design completion, GC solicitation, municipal approvals, construction oversight, hazardous materials remediation, and funding source coordination.
</p>
	<p>Cleveland campus included everything from upgrading the electric to allow for computers, to completing cosmetic work to make a brighter space for the kids to learn.</p>
