          <h3>Description </h3>
<p>Freire Charter School, located in Philadelphia, is a new charter middle school on Market Street. NorthStar started assisting Freire in 2011 with providing real estate site selections of nearly 40 different Philadelphia locations, finally deciding on the Market Street site as the future Freire Charter Middle School. NorthStar also assisted in financial package preparation, construction management and budget management, including: furniture and rubbish removal, painting classrooms, corridors, bathrooms, replacing and installing electrical outlets, ballasts and non-functioning fixtures, demoing janitor closets into the deans office, demoing the reception office and a wall for an office opening, completing mill work for the reception area (desk and half wall caps), putting up dry wall and doors, installing ceiling tiles, completing exterior glazing in the main building and interior classroom door window glazing, and VCT floor patching and vinyl base. Freire Charter Middle School opened September 2012.</p>
